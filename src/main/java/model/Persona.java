package model;

/**
 *
 * @author JETTSUSHD
 */
public class Persona {
    private int id;
    private String nombre;
    private String dni;
    private char genero;

    public Persona() {
    }

    public Persona(int id, String nombre, String dni, char genero) {
        this.id = id;
        this.nombre = nombre;
        this.dni = dni;
        this.genero = genero;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public char getGenero() {
        return genero;
    }

    public void setGenero(char genero) {
        this.genero = genero;
    }
}
